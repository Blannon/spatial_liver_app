        "    - Returns n_hot so the caller can warn if too few hot cells.\n",
        "\n",
        "    We only run DBSCAN if we have >= min_samples hot cells. If DBSCAN\n",
        "    finds no clusters, everything stays 'Background'.\n",
        "    \"\"\"\n",
        "    adata = DATASETS[sample_name]\n",
        "\n",
        "    # 1) signature scores\n",
        "    scores, used_genes = compute_signature_scores(adata, signature_name)\n",
        "\n",
        "    x = adata.obs[\"x_centroid\"].values\n",
        "    y = adata.obs[\"y_centroid\"].values\n",
        "    coords = np.column_stack([x, y])\n",
        "\n",
        "    # 2) select hot cells\n",
        "    thresh = np.quantile(scores, hot_quantile)\n",
        "    hot_mask = scores >= thresh\n",
        "    n_hot = int(hot_mask.sum())\n",
        "\n",
        "    # start with all cells as background\n",
        "    zone_labels = np.full(adata.n_obs, -1, dtype=int)\n",
        "    driver_dict = {}\n",
        "\n",
        "    # only attempt DBSCAN if we have enough hot cells\n",
        "    if n_hot >= min_samples:\n",
        "        hot_coords = coords[hot_mask]\n",
        "\n",
        "        # Run DBSCAN\n",
        "        clustering = DBSCAN(eps=eps, min_samples=min_samples, n_jobs=-1).fit(hot_coords)\n",
        "        labels_hot = clustering.labels_  # -1 = noise\n",
        "\n",
        "        # Map back to full dataset\n",
        "        zone_labels[hot_mask] = labels_hot\n",
        "\n",
        "        unique_zones = sorted(z for z in np.unique(labels_hot) if z != -1)\n",
        "\n",
        "        # Compute driver genes for each zone\n",
        "        if unique_zones:\n",
        "            X = adata.X\n",
        "            for z in unique_zones:\n",
        "                in_zone = zone_labels == z\n",
        "                n_in = int(in_zone.sum())\n",
        "\n",
        "                # Skip zones that are too small\n",
        "                if n_in < 10:  # Minimum 10 cells for meaningful stats\n",
        "                    continue\n",
        "\n",
        "                out_zone = ~in_zone\n",
        "\n",
        "                if sp.issparse(X):\n",
        "                    mean_in = np.asarray(X[in_zone].mean(axis=0)).ravel()\n",
        "                    mean_out = np.asarray(X[out_zone].mean(axis=0)).ravel()\n",
        "                else:\n",
        "                    mean_in = X[in_zone].mean(axis=0)\n",
        "                    mean_out = X[out_zone].mean(axis=0)\n",
        "\n",
        "                # Calculate fold change (with pseudocount)\n",
        "                mean_out = mean_out + 1e-6\n",
        "                fc = mean_in / mean_out\n",
        "\n",
        "                # Get top genes by fold change (filter out very low expression)\n",
        "                min_expr_threshold = 0.1\n",
        "                valid_idx = mean_in > min_expr_threshold\n",
        "\n",
        "                if valid_idx.sum() > 0:\n",
        "                    fc_filtered = np.where(valid_idx, fc, 0)\n",
        "                    top_idx = np.argsort(fc_filtered)[::-1][:10]\n",
        "                    top_genes = adata.var_names[top_idx]\n",
        "                    top_fc = fc[top_idx]\n",
        "\n",
        "                    zone_key = f\"Zone {z}\"\n",
        "                    driver_dict[zone_key] = [\n",
        "                        {\"gene\": g, \"fc\": float(fv)}\n",
        "                        for g, fv in zip(top_genes, top_fc)\n",
        "                        if fv > 1.0  # Only include upregulated genes\n",
        "                    ]\n",
        "\n",
        "    # 3) dataframe for plotting (all cells)\n",
        "    df_cells = pd.DataFrame(\n",
        "        {\n",
        "            \"x\": x,\n",
        "            \"y\": y,\n",
        "            \"score\": scores,\n",
        "            \"zone\": zone_labels,\n",
        "            \"is_hot\": hot_mask,\n",
        "        }\n",
        "    )\n",
        "\n",
        "    def label_zone(z):\n",
        "        return \"Background\" if z == -1 else f\"Zone {z}\"\n",
        "\n",
        "    df_cells[\"zone_str\"] = df_cells[\"zone\"].apply(label_zone)\n",
        "\n",
        "    return df_cells, driver_dict, n_hot\n",
        "\n",
