        "        g = exact[0]\n",
        "    else:\n",
        "        # fallback: substring search\n",
        "        partial = [g for g in adata.var_names if gene.upper() in g.upper()]\n",
        "        if partial:\n",
        "            g = partial[0]\n",
        "        else:\n",
        "            all_genes = sorted(list(adata.var_names))\n",
        "            suggestions = \", \".join(all_genes[:15])\n",
        "            raise ValueError(\n",
        "                f\"Gene '{gene_symbol}' not found in panel. Try one of: {suggestions}\"\n",
        "            )\n",
        "\n",
        "    vec = adata[:, g].X\n",
        "    if sp.issparse(vec):\n",
        "        vec = vec.toarray().ravel()\n",
        "    else:\n",
        "        vec = np.asarray(vec).ravel()\n",
        "\n",
        "    return g, vec\n",
