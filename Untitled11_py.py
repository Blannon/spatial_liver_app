        "def _get_gene_expr(adata, gene_symbol: str):\n",
        "    gene = gene_symbol.strip()\n",
        "    if not gene:\n",
        "        raise ValueError(\"Empty gene symbol\")\n",
        "\n",
        "    # exact match (case-insensitive)\n",
        "    exact = [g for g in adata.var_names if g.upper() == gene.upper()]\n",
